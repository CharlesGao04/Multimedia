%读取图片
I=imread('test_2.jpg');
%灰度拉伸
J=imadjust(I,[0.2,0.6],[0,1]);
%直方图均衡化
K=histeq(I);
%画出增强前后对比图
figure;
subplot(1,3,1),imshow(I),xlabel('原图');
subplot(1,3,2),imshow(J),xlabel('灰度拉伸');
subplot(1,3,3),imshow(K),xlabel('直方图均衡化');
%画出灰度值分布图
figure;
subplot(3,1,1),imhist(I),title('原图');
axis([0,250,0,25000]);
subplot(3,1,2),imhist(J),title('灰度拉伸');
axis([0,250,0,25000]);
subplot(3,1,3),imhist(K),title('直方图均衡化');
axis([0,250,0,25000]);
